package AlgorithmBased.SelectionSortingAlgorithm;

public class student {
    private String name;
    private int age;
    private String gender;
    private int usn;

public void setData(String x, int y, String z, int p){
    name = x;
    age = y;
    gender = z;
    usn = p;
}
public String getName(){
    return name;
}
public int getAge(){
    return age;
}
public String getGender(){
    return gender;
}
public int getUsn(){
    return usn;
}
}
