package RandomQuestionSolving;

public class example1 {
    public static void main(String[] args) {
        int temp;
        int a = 10;
        int b = 20;
        System.out.println("before swapping a : " + a);
        System.out.println("before swapping b : "+ b);
        System.out.println("after swapping ");
        temp= b;
        b = a;
        a = temp;
        System.out.println("before swapping a : " + a);
        System.out.println("before swapping b : "+ b);
    }
}
